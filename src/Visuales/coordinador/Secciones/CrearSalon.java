    private void jBtnFinalizarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnFinalizarRegistroActionPerformed
        String nombreSeccion = JTextNombreSeccion.getText().trim();
        if (nombreSeccion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se puede finalizar el registro sin un nombre de sección.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Guardar el nombre de la sección en el objeto organizador
        sistema.getOrganizador().getSecciones().add(nombreSeccion);

        // Habilitar los campos y botones
        JTextNombreSeccion.setEnabled(true);
        jComboBoxUniversidad.setEnabled(true);
        cmbTurno.setEnabled(true);

        // Limpiar las tablas
        DefaultTableModel modeloDisponibles = (DefaultTableModel) jTableAlumnosDisponibles.getModel();
        modeloDisponibles.setRowCount(0);

        DefaultTableModel modeloSeccion = (DefaultTableModel) jTableAlumnosEnLaSeccion.getModel();
        modeloSeccion.setRowCount(0);

        // Limpiar los espacios de texto
        JTextNombreSeccion.setText("");
        jLabel4.setText("Inscritos en:"); // Restaurar el texto original del label

        // Mensaje de confirmación
        JOptionPane.showMessageDialog(this, "Registro finalizado con éxito. La sección '" + nombreSeccion + "' ha sido guardada.", "Información", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jBtnFinalizarRegistroActionPerformed

    private void jBtnGuardarNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarNombreActionPerformed
        
        
        if (JTextNombreSeccion.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre de la sección no puede estar vacío.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return; // Salir del método si está vacío
        }

        String universidadSeleccionada = jComboBoxUniversidad.getSelectedItem().toString();
        String turnoSeleccionado = cmbTurno.getSelectedItem().toString();

        DefaultTableModel modelo = (DefaultTableModel) jTableAlumnosDisponibles.getModel();
        modelo.setRowCount(0); // Limpiar la tabla antes de agregar nuevos resultados

        // Filtrar estudiantes por universidad, turno y seccion no asignada
        for (Estudiante est : sistema.getListaEstudiantes()) {
            if (est.getUniversidad().equals(universidadSeleccionada) 
                    && est.getTurno().equals(turnoSeleccionado)
                    && (est.getSeccion() == null || est.getSeccion().trim().isEmpty())) {
                // Agregar los estudiantes filtrados a la tabla
                modelo.addRow(new Object[]{est.getCodigo(), est.getNombres(), est.getApellidoP()});
            }
        }

        // Mostrar un mensaje si no se encontró ningún estudiante
        if (modelo.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No se encontraron estudiantes sin sección asignada para la universidad y turno seleccionados.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Estudiantes filtrados según universidad, turno y sin sección asignada.", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBtnGuardarNombreActionPerformed

    private void BtnAgregarEstudianteASeccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarEstudianteASeccionActionPerformed
        String nombreSeccion = JTextNombreSeccion.getText().trim();
        jLabel4.setText("Sección: " + nombreSeccion);
        
        
        
        JTextNombreSeccion.setEnabled(false);
        jComboBoxUniversidad.setEnabled(false);
        cmbTurno.setEnabled(false);

        // Validar que haya un estudiante seleccionado
        int filaSeleccionada = jTableAlumnosDisponibles.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor seleccione un estudiante para agregar a la sección.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Obtener el modelo de las tablas
        DefaultTableModel modeloDisponibles = (DefaultTableModel) jTableAlumnosDisponibles.getModel();
        DefaultTableModel modeloSeccion = (DefaultTableModel) jTableAlumnosEnLaSeccion.getModel();

        // Obtener datos del estudiante seleccionado
        String codigo = modeloDisponibles.getValueAt(filaSeleccionada, 0).toString();
        String nombre = modeloDisponibles.getValueAt(filaSeleccionada, 1).toString();
        String apellido = modeloDisponibles.getValueAt(filaSeleccionada, 2).toString();

        // Buscar al estudiante en la lista y asignar la sección
        
        String universidad = jComboBoxUniversidad.getSelectedItem().toString(); // Universidad seleccionada
        String turno = cmbTurno.getSelectedItem().toString(); // Turno seleccionado

        for (Estudiante est : sistema.getListaEstudiantes()) {
            if (est.getCodigo().equals(codigo)) {
                est.setSeccion(nombreSeccion); // Asignar el nombre de la sección al estudiante
                break;
            }
        }

        // Agregar estudiante con todos sus datos a la tabla de la sección
        modeloSeccion.addRow(new Object[]{codigo, nombre, apellido, universidad, turno});

        // Eliminar el estudiante de la tabla de disponibles
        modeloDisponibles.removeRow(filaSeleccionada);

        JOptionPane.showMessageDialog(this, "Estudiante agregado a la sección correctamente.", "Información", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_BtnAgregarEstudianteASeccionActionPerformed

