    private void jBtnEliminarEstudianteDeSeccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarEstudianteDeSeccionActionPerformed
        int filaSeleccionada = jTableAlumnosEnLaSeccion.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor seleccione un estudiante para eliminar de la sección.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Obtener el modelo de la tabla de la sección
        DefaultTableModel modeloSeccion = (DefaultTableModel) jTableAlumnosEnLaSeccion.getModel();
        DefaultTableModel modeloDisponibles = (DefaultTableModel) jTableAlumnosDisponibles.getModel();

        // Obtener los datos del estudiante seleccionado
        String codigo = modeloSeccion.getValueAt(filaSeleccionada, 0).toString();
        String nombre = modeloSeccion.getValueAt(filaSeleccionada, 1).toString();
        String apellido = modeloSeccion.getValueAt(filaSeleccionada, 2).toString();

        // Buscar al estudiante en la lista del sistema y limpiar su atributo de sección
        for (Estudiante est : sistema.getListaEstudiantes()) {
            if (est.getCodigo().equals(codigo)) {
                est.setSeccion(""); // Eliminar la sección asignada al estudiante
                break;
            }
        }

        // Agregar al estudiante de vuelta a la tabla de disponibles
        modeloDisponibles.addRow(new Object[]{codigo, nombre, apellido});

        // Eliminar al estudiante de la tabla de la sección
        modeloSeccion.removeRow(filaSeleccionada);

        JOptionPane.showMessageDialog(this, "Estudiante eliminado de la sección correctamente.", "Información", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jBtnEliminarEstudianteDeSeccionActionPerformed

